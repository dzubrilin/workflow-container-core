        public static string GetContentType(string extension)
        {
            Guard.ArgumentNotNullOrEmpty(extension, nameof(extension));
            return _mapping.Value.TryGetValue(extension, out var mimeType) ? mimeType : _mapping.Value[".*"];
        }

